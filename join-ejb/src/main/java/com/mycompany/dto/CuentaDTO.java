package com.mycompany.dto;

/**
 *
 * @author juan
 */
public class CuentaDTO {
    private int id;
    private String numero;
    public CuentaDTO() {
    }

    public CuentaDTO(int id, String numero) {
        this.id = id;
        this.numero = numero;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }
}
